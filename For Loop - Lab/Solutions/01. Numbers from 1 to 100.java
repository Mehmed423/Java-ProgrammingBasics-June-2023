public class Demo {
    public static void main(String[] args) {
        //FOR цикъл - повтаряме нещо определен брой пъти
        // 1 до 100
 
        //начало: 1
        //край: 100
        //повтаряме: отпечатвам числото
        //промяна: +1
 
        for (int number = 1; number <= 100; number++) {
            //какво искаме да повторим
            System.out.println(number);
        }
    }
}
